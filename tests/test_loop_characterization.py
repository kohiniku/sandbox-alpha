        f = self.FIXTURES["full_adoption"]
        hyp = _make_hypothesis()
        verdict, evaluation = evaluate_result(hyp, f["result"], _fresh_knowledge())
        assert verdict == f["verdict"]
        # cluster_id is random, mask it for comparison
        expected = dict(f["evaluation"])
        actual = dict(evaluation)
        assert "cluster_id" in actual, "adopted evaluation must have cluster_id"
        expected.pop("cluster_id", None)
        actual.pop("cluster_id", None)
        assert actual == expected

