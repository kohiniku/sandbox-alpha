"""
Characterization tests for evaluate_result and _evaluate_manifest_result.

Captures EXACT (verdict, evaluation) output for a fixture set of synthetic
result dicts, generated by calling the real functions on the current main
and recording their output in tests/fixtures/char_fixtures.json.

These tests lock the current behavior so that refactoring can proceed
safely: after any refactor, every fixture must still produce the exact
same (verdict, evaluation) output.
"""

import json
import sys
import tempfile
from pathlib import Path
from unittest.mock import patch, MagicMock

import pytest

sys.path.insert(0, str(Path(__file__).resolve().parent.parent))

from autonomous_loop import evaluate_result, _evaluate_manifest_result, load_knowledge
from backlog import Backlog, make_param_entry


FIXTURES_PATH = Path(__file__).resolve().parent / "fixtures" / "char_fixtures.json"


def _load_fixtures():
    with open(FIXTURES_PATH) as f:
        return json.load(f)


