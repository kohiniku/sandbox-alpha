def _make_hypothesis(strategy="sma_crossover", symbol="AAPL", params=None):
    if params is None:
        params = {"fast_window": 10, "slow_window": 30}
    return {
        "id": "char_test_001",
        "strategy": strategy,
        "symbol": symbol,
        "params": params,
        "description": "test",
        "generated_at": "2026-01-01T00:00:00",
    }


def _make_manifest_hypothesis():
    return {
        "id": "bl_char_manifest",
        "strategy": "manifest:test_strat",
        "symbol": "universe:abcdef12",
        "params": {"universe_size": 5, "execution_mode": "structured", "primary_metric": "sharpe"},
        "description": "test",
        "generated_at": "2026-01-01T00:00:00",
    }


def _fresh_knowledge():
    return {
        "tested": [], "tested_combinations": [], "adopted": [], "rejected": [],
        "superseded": [], "families": {}, "iterations": 0, "errors": [],
    }
