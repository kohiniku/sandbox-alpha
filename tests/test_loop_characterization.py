    # --- Knowledge-state-dependent fixtures (round-trip disk) ---
    # These don't go in the snapshot-based FIXTURES because cluster dedup
    # requires actual knowledge state on disk.

    def test_full_adoption_disk_roundtrip(self, tmp_path, monkeypatch):
        """Full adoption with knowledge written/read from disk."""
        kf = tmp_path / "knowledge.json"
        monkeypatch.setattr("autonomous_loop.KNOWLEDGE_FILE", kf)

        knowledge = _fresh_knowledge()
        # Save empty knowledge to disk so evaluate_result can find it
        kf.write_text(json.dumps(knowledge))

        f = self.FIXTURES["full_adoption"]
        hyp = _make_hypothesis()
        verdict, evaluation = evaluate_result(hyp, f["result"], knowledge)

        assert verdict == "adopted"
        assert evaluation["verdict"] == "adopted"
        assert evaluation["sharpe_ratio"] == 1.2
        assert evaluation["holdout_sharpe"] == 0.8
        assert evaluation["gate_results"]["validation"] is True
        assert evaluation["gate_results"]["holdout"] is True
        assert evaluation["gate_results"]["cluster"] == "new"
        assert "🏆 ALL GATES PASSED" in evaluation["reasons"][0]
        assert "cluster_id" in evaluation


